def clean_text(text):

    text = text.lower()

    text = re.sub(
        r"\d+",
        " ",
        text
    )

    text = re.sub(
        r"[^a-z\s]",
        " ",
        text
    )

    words = text.split()

    words = [
        w for w in words
        if w not in stopwords
        and len(w) > 4
    ]

    return " ".join(words)


