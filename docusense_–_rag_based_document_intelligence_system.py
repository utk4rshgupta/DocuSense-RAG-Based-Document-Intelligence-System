import networkx as nx

G = nx.Graph()

for item in topics:
    topic = item["topic"]

    G.add_node(topic)

    for keyword in item["keywords"]:
        G.add_node(keyword)
        G.add_edge(topic, keyword)

import matplotlib.pyplot as plt
import networkx as nx

# Compute node positions
pos = nx.spring_layout(
    G,
    seed=42,
    k=1.5,
    iterations=100
)

# Color map
topic_colors = plt.cm.tab20.colors

node_colors = []

for node in G.nodes():

    color = "lightgray"

    for idx, item in enumerate(topics):
        if node == item["topic"]:
            color = topic_colors[idx % 20]
            break
        elif node in item["keywords"]:
            color = topic_colors[idx % 20]
            break

    node_colors.append(color)

# Node sizes
